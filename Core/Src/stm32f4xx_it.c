/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
/* Handle IDLE line event: one UART2 frame finished */
  if (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) != RESET)
  {
      __HAL_UART_CLEAR_IDLEFLAG(&huart2);

      /* Use huart2.hdmarx for robustness on F4 stream mapping */
      uint16_t received = (uint16_t)(U2_RXMAX_SIZE - __HAL_DMA_GET_COUNTER(huart2.hdmarx));

      HAL_UART_DMAStop(&huart2);

      if (received > 0)
      {
          UART2_ControlCB.UsartRxDataInPtr->EndPtr =
              UART2_ControlCB.UsartRxDataInPtr->StartPtr + received;

          UART2_ControlCB.UsartRxDataInPtr++;
          if (UART2_ControlCB.UsartRxDataInPtr == UART2_ControlCB.UsartRxDataEndPtr)
          {
              UART2_ControlCB.UsartRxDataInPtr = &UART2_ControlCB.Usart_RxLocation[0];
          }
      }

      UART2_ControlCB.Usart_RxCounter += received;
      if (U2_RXBUFF_SIZE - UART2_ControlCB.Usart_RxCounter >= U2_RXMAX_SIZE)
      {
          UART2_ControlCB.UsartRxDataInPtr->StartPtr = &U2_RxBuff[UART2_ControlCB.Usart_RxCounter];
      }
      else
      {
          UART2_ControlCB.Usart_RxCounter = 0;
          UART2_ControlCB.UsartRxDataInPtr->StartPtr = U2_RxBuff;
      }

      HAL_UART_Receive_DMA(&huart2, UART2_ControlCB.UsartRxDataInPtr->StartPtr, U2_RXMAX_SIZE);
      return; 
  }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

